package org.wso2.carbon.streaming.integrator.core.persistence.beans;

import org.wso2.carbon.config.annotation.Configuration;

@Configuration(namespace = "service.catalog.configs",
        description = "Service catalog configurations for Async API definition publishing")
public class AsyncAPIServiceCatalogueConfigs {
    private String hostname;
    private String port;
    private String username;
    private String password;
    private boolean enabled = false;

    public String getHostname() {
        return hostname;
    }

    public void setHostname(String hostname) {
        this.hostname = hostname;
    }

    public String getPort() {
        return port;
    }

    public void setPort(String port) {
        this.port = port;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }
}
